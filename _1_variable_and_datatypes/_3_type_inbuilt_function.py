# type() - helps you to know that variable is storing what type of datatype

# int
num = 2 
print("Int : ",2)
print(type(num))

# # # float
salary = 10000.34
print("Float : ",salary)
print(type(salary))

# # # str
name = "Bharati"
print("Str : ",name)
print(type(name))

# # bool
night = True
print("Bool : ",night)
print(type(night))

# # # complex
comp = 2+9j
print("Complex : ",comp)
print(type(comp))