# File Handling
# - it is use to store the data permanently in the file

# Types of files
# 1. Text File (.txt,.doc)
# 2. Binary File (images,audio,videos)

# Modes of text files

# r - read mode  , default mode
#                , allows to read the data from existing file

# w - write mode , creates the file if the mentioned file doesn't exist
#                  if file already exist, it will rewrite it
#                  it allows to write the data in file

# a - append mode , creates the file if the mentioned file doesn't exist
#                   if file already exist, it will append new data to it
#                   it allows to write the data in file
                    
# r+ - allows to read and write in the file

