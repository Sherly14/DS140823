# Arithmetic Operator

# +	Addition	x + y	
# -	Subtraction	x - y	
# *	Multiplication	x * y	
# /	Division	x / y	
# %	Modulus	x % y	
# ** Exponentiation	x ** y	
# // Floor division	x // y	

no1 = int(input("Enter your no1 : "))
no2 = int(input("Enter your no2 : "))

add = no1 + no2
print("Addition : ",add)

sub = no1 - no2
print("Subraction : ",sub)

mul = no1 * no2
print("Product : ",mul)

# it will always a float value
div = no1/no2
print("Division : ",div)

mod = no1 % no2
print("Modulus : ",mod)

expo = no1 ** no2
print("Exponenitial : ",expo)

floor_div = no1 // no2
print("Floor Division : ",floor_div)
