# Identity Operator - returns bool value

# is      - returns True, if memory location of both variables are same
# is not  - returns True, if memory location of both variables are different

no1 = 300
no2 = 300

is_demo = no1 is no2
print("Is : ", is_demo)

is_not_demo = no1 is not no2
print("Is Not : ", is_not_demo)