# Class - blueprint for creating an object

# class <class_name>:
#     body

# function - are functions which are defined outside the class
# method   - are functions which are defined inside the class

class building:
    def doors(self):
        print("20 doors!")

    def windows(self):
        print("40 windows!")

obj = building()             # created object/instance 
obj.doors()
obj.windows()

