# Comparison Operator - returns bool value

# ==
# !=
# >
# <
# >=
# <=

no1 = 10
no2 = 5

equal = no1 == no2
print("Equal : ",equal)

not_equal = no1 != no2
print("Not equal:",not_equal)

greater = no1 > no2
print("no1 is greater than no2:",greater)

lesser = no1 < no2
print("no1 is lesser than no2:",lesser)

greater_or_equal = no1 >= no2
print("no1 is greater than or equal to num :",greater_or_equal)

lesser_or_equal = no1 <= no2
print("no1 is less than or equal to num :",lesser_or_equal)

