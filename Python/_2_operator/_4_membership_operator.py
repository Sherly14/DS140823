# Membership Operator - returns bool value

# in         - returns True if specified object is present in sequence of objects
# not in     - returns True if specified object is not present in sequence of objects

no1 = 10
lst = [4,5,3,6,7,10]

in_demo = no1 in lst
print("In : ", in_demo)

not_in_demo = no1 not in lst
print("Not In : ", not_in_demo)

